public class IdentificadorDemo {
    public static void main(String[] args) {
        // Declaración de variables con identificadores válidos
        int numeroDeEstudiantes = 10;
        String miNombre = "Juan";

        // Llamada a método con identificador válido
        imprimirNombre(miNombre);
    }

    // Método con identificador válido
    public static void imprimirNombre(String nombre) {
        System.out.println("Nombre: " + nombre);
    }
}
